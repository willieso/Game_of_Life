	private final int rows;
	private final int columns;
	private final boolean[][] gen1;
	private final boolean[][] gen2;
	private boolean[][] currentGen;
	private boolean[][] nextGen;
	private boolean[][] freeGen;
	private boolean[][] randomShape;
	private int[][] pic;
	private volatile boolean isChanging = false;  //volatile修饰被不同线程访问和改变的变量
	private volatile int speed = 10;              //调整游戏速度,用volatile是否有必要？
	private Random random = new Random();         //用来产生随机数的对象
	
	public Map(int rows, int columns)
	{
		this.rows = rows;
		this.columns = columns;
		
		gen1 = new boolean[rows][columns];
		for(int i = 0; i < rows; i++)
		{
			for(int j = 0; j < columns; j++)
			{
				gen1[i][j] = false;
			}
		}
		
		gen2 = new boolean[rows][columns];
		for(int i = 0; i < rows; i++)
		{
			for(int j = 0; j < columns; j++)
			{
				gen2[i][j] = false;
			}
		}
		
		//初始化各个细胞图片
		freeGen = new boolean[rows][columns];
		randomShape = new boolean[rows][columns];
		pic = new int[rows][columns];

		for(int i = 0; i < rows; i++)
		{
			for(int j = 0; j< columns; j++)
			{
				freeGen[i][j] = false;
				randomShape[i][j] = false;
				pic[i][j] = 0;
			}
		}
		
		//初始化两个世代的细胞图
		currentGen = gen1;     //当前世代
		nextGen = gen2;        //下一世代
	}
	
	//线程体
	public void run()          
	{
		while(true)
		{
			synchronized(this)
			{
				while(isChanging)        //判断细胞图是否正在更新
				{
					try
					{
						this.wait();
					}catch(InterruptedException e)
					{
						e.printStackTrace();
					}
				}
				
				repaint();
				sleep(speed);
				
				for(int i = 0; i < rows; i++)
				{
					for(int j =0; j < columns; j++)
					{
						evolve(i,j);     //执行当前细胞的世代演变
					}
				}
				
				//???
				boolean[][] temp = null;
				temp = currentGen;
				currentGen = nextGen;
				nextGen = temp;
				
				for(int i = 0; i < rows; i++)
				{
					for(int j =0; j < columns; j++)
					{
						nextGen[i][j] = false;
					}
				}
				
			}
		}	
	}
	
