	//线程休眠逻辑
	private void sleep(int i)
	{
		try
		{
			Thread.sleep(100*i);
		}catch(InterruptedException e)
		{
			e.printStackTrace();
		}
	}
	
	public void changeSpeed(int i)
	{
		speed = i;
	}
	
	//暂停&继续方法
	public void pause()
	{
		if(isChanging == false)
			isChanging = true;
		else
		{
			synchronized(this)                     //不加synchronized的话只能暂停，不能继续？？？大概是没拿到map的锁？
			{
				isChanging = false;
				this.notifyAll();
			}
		}
	}
	
	//重置地图方法
	public void clearMap()
	{
		isChanging = true;
		synchronized(this)
		{
			for(int i = 0; i < rows; i++)
			{
				for(int j = 0; j < columns; j++)
				{
					currentGen[i][j] = false;
				}
			}
			isChanging = false;
			this.notifyAll();
		}
	}
	
	//演化算法
	private void evolve(int x, int y)
	{
		int aliveSurrounding = 0;     //当前细胞邻居的活跃数
		
		//判断当前细胞周围八个细胞位置的合法性
		if(isValid(x - 1, y - 1) && (currentGen[x - 1][y - 1] == true))
		{
			aliveSurrounding++;
		}
		if(isValid(x - 1, y) && (currentGen[x - 1][y] == true))
		{
			aliveSurrounding++;
		}
		if(isValid(x - 1, y + 1) && (currentGen[x - 1][y + 1] == true))
		{
			aliveSurrounding++;
		}
		if(isValid(x, y + 1) && (currentGen[x][y + 1] == true))
		{
			aliveSurrounding++;
		}
		if(isValid(x + 1, y + 1) && (currentGen[x + 1][y + 1] == true))
		{
			aliveSurrounding++;
		}
		if(isValid(x + 1, y) && (currentGen[x + 1][y] == true))
		{
			aliveSurrounding++;
		}
		if(isValid(x + 1, y - 1) && (currentGen[x + 1][y - 1] == true))
		{
			aliveSurrounding++;
		}
		if(isValid(x, y - 1) && (currentGen[x][y - 1] == true))
		{
			aliveSurrounding++;
		}
		
		//判断当前细胞的演化结果
		if(aliveSurrounding == 3)
		{
			nextGen[x][y] = true;
		}
		else if(aliveSurrounding == 2)
		{
			nextGen[x][y] = currentGen[x][y];
		}
		else
		{
			nextGen[x][y] = false;
		}
	}
	
	//判断当前细胞是否在地图范围内
	private boolean isValid(int x, int y)                    
	{
		if(x >= 0 && x < rows && y >= 0 && y < columns)
		{
			return true;
		}
		else
			return false;
	}
	
	//利用预设图形或自由图形作为初始图形
	private void setFire(int[][] pic)
	{
		isChanging = true;
		
		synchronized(this)
		{
			for(int i = 0; i < rows; i++)
			{
				for(int j = 0; j < columns; j++)
				{
					if(pic[i][j] == 1 || currentGen[i][j] == true)
					{
						currentGen[i][j] = true;
					}
					else
					{
						currentGen[i][j] = false;
					}
				}
			}
			isChanging = false;
			this.notifyAll();
		}
	}
	
	public void setSakura()
	{
		setFire(sakura);
	}
	
	public void setDiamond()
	{
		setFire(diamond);
	}
	
	public void setSquare()
	{
		setFire(square);
	}
	
	public void setRandom()
	{
		isChanging = true;
		synchronized(this)
		{
			for(int i = 0; i < rows; i++)
			{
				for(int j = 0; j < columns; j++)
				{
					currentGen[i][j] = random.nextBoolean();
				}
			}
			isChanging = false;
			this.notifyAll();
		}
	}
	
	public void setPic()
	{
		setFire(pic);
	}
	
